 package net.mingsoft.base.entity;
















































 public class ResultJson
 {
   private String code;
   private boolean result;
   private String resultMsg;
   private Object resultData = "";





   private String url;





   public String getUrl() { return this.url; }







   public void setUrl(String url) { this.url = url; }







   public String getCode() { return this.code; }







   public void setCode(String code) { this.code = code; }







   public boolean isResult() { return this.result; }







   public void setResult(boolean result) { this.result = result; }







   public String getResultMsg() { return this.resultMsg; }







   public void setResultMsg(String resultMsg) { this.resultMsg = resultMsg; }







   public Object getResultData() { return this.resultData; }







   public void setResultData(Object resultData) { this.resultData = resultData; }
 }


