 package net.mingsoft.base.entity;

 import java.util.List;




































 public class ListJson
 {
   private int count;
   private List list;

   public ListJson(int count, List list) {
     this.count = count;
     this.list = list;
   }






   public int getCount() { return this.count; }







   public List getList() { return this.list; }
 }


